// Package syncstore хранит значение в памяти и обновляет его по сигналу
// из внешнего источника.
//
// Ядро не знает про конкретное хранилище: источник сигналов — интерфейс
// [Listener], чтение данных — функция [Loader]. Конкретные СУБД и брокеры
// подключаются драйверами — отдельными Go-модулями, реализующими [Listener].
package syncstore

import (
	"context"
	"errors"
)

// ErrListenerStopped возвращается из [Store.Run], когда канал сигналов
// закрылся до отмены контекста — источник сигналов остановился насовсем.
var ErrListenerStopped = errors.New("syncstore: listener stopped")

// Signal — уведомление «данные изменились». Для хранилища это чистый триггер
// перезагрузки: payload не интерпретируется, но пробрасывается — реализациям
// он может пригодиться для логирования или будущей точечной перечитки.
type Signal struct {
	Channel string
	Payload string
}

// Listener — источник сигналов об изменении данных.
//
// Listen запускает прослушивание и возвращает канал сигналов. Канал
// закрывается, когда прослушивание останавливается — по отмене ctx или из-за
// невосстановимой ошибки. Переподключения — забота реализации; если за время
// обрыва сигналы могли быть потеряны, реализация обязана выдать синтетический
// Signal после восстановления — хранилище перечитает данные и «догонит»
// пропущенное.
type Listener interface {
	Listen(ctx context.Context) (<-chan Signal, error)
}

// Loader читает актуальное значение целиком. Один и тот же Loader
// используется и при первом обращении к пустому хранилищу, и при каждом
// сигнале.
type Loader[T any] func(ctx context.Context) (T, error)

// Store — значение T в памяти, обновляемое по сигналу.
type Store[T any] interface {
	// Get отдаёт закэшированное значение как есть. Если хранилище ещё
	// пустое — синхронно загружает его через Loader.
	//
	// Значение разделяется всеми вызывающими: если T содержит ссылочные
	// типы (slice, map), обращаться с ним как с read-only.
	Get(ctx context.Context) (T, error)

	// Run слушает Listener и на каждый сигнал перечитывает значение.
	// Блокируется до отмены ctx (возвращает ctx.Err()) или до закрытия
	// канала сигналов (возвращает [ErrListenerStopped]); запускается одной
	// горутиной на процесс.
	Run(ctx context.Context) error
}
